// ─── i18n Manager ────────────────────────────────────────
class I18n {
  constructor() {
    this.currentLang = localStorage.getItem('velora_lang') || 'pt-BR';
  }

  setLanguage(lang) {
    if (translations[lang]) {
      this.currentLang = lang;
      localStorage.setItem('velora_lang', lang);
      document.documentElement.lang = lang;
      window.dispatchEvent(new CustomEvent('langChange', { detail: { lang } }));
    }
  }

  t(key, vars = {}) {
    const dict = translations[this.currentLang] || translations['pt-BR'];
    let text = dict[key] || translations['pt-BR'][key] || key;
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(`{{${k}}}`, v);
    });
    return text;
  }

  getCurrentLang() { return this.currentLang; }
  getLangInfo()    { return LANGUAGES[this.currentLang]; }
}

export const i18n = new I18n();
export const t = (key, vars) => i18n.t(key, vars);
